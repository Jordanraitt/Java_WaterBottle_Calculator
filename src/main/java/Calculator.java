public class Calculator {


    public Calculator(){
    }

    public int calcAdd(int number1, int number2){
        int result = (number1 + number2);
        {
            return result;
        }
    }

    public int calcSub(int number1, int number2){
        int result = (number1 - number2);
        {
            return result;
        }
    }

    public int calcMulti(int number1, int number2){
        int result = (number1 * number2);
        {
            return result;
        }
    }

    public int calcDiv(int number1, int number2){
        int result = (number1 / number2);
        {
            return result;
        }
    }


}
